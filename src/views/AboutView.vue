        scale: 1.05,
        y: -3,
        duration: 0.3,
        ease: 'back.out(1.7)'
      })
    })
    
    linkElement.addEventListener('mouseleave', () => {
      gsap.to(linkElement, {
        scale: 1,
        y: 0,
        duration: 0.3,
        ease: 'power2.out'
      })
    })
  })
})
